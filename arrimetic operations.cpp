#include<iostream>
using namespace std;
int main()
{
	int a,b,add,sub,mul,div,mod;
	cout<<"\nEnter Number 1:";
	cin>>a;
	cout<<"\nEnter Number 2:";
	cin>>b;
	add=a+b;
	sub=a-b;
	mul=a*b;
	div=a/b;
	mod=a%b;
	cout<<"\nAddition:"<<add;
	cout<<"\nSubtraction:"<<sub;
	cout<<"\nMultiplication:"<<mul;
	cout<<"\nDivision:"<<div;
	cout<<"\nModulus:"<<mod;
	return 0;
}










